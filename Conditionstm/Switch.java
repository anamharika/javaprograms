package Conditionstm;

public class Switch {
	public static void main(String[] args) {
		 int hari = 10;
		 switch (hari) {
		 case 10:{
			 System.out.println("case1");
			 break;
		 }
		 case 20:{
			 System.out.println("case 2");
			 break;
		 }
		 case 30:{
			 System.out.println("case 3");
			 break;
		 }
		 }
	}

}
